use Vas_MyBase
SELECT ТОВАРЫ.Наименоввание_товара, КЛИЕНТЫ.Наименование_фирмы_заказчика, ЗАКАЗЫ.Вид_доставки
FROM ТОВАРЫ, КЛИЕНТЫ, ЗАКАЗЫ
WHERE ТОВАРЫ.id_товара = ЗАКАЗЫ.id_товара
and
КЛИЕНТЫ.id_клиента = ЗАКАЗЫ.id_клиента
and
Вид_доставки IN (SELECT ЗАКАЗЫ.Вид_доставки FROM ЗАКАЗЫ WHERE (ЗАКАЗЫ.Вид_доставки like'%само%'))


SELECT КЛИЕНТЫ.Наименование_фирмы_заказчика, ТОВАРЫ.Наименоввание_товара, Товары.Цена 
FROM ЗАКАЗЫ, ТОВАРЫ, КЛИЕНТЫ
WHERE
	КЛИЕНТЫ.id_клиента = ЗАКАЗЫ.id_клиента
	and
	ТОВАРЫ.id_товара = ЗАКАЗЫ.id_товара
	and
	Цена = (select top(3) Цена from ТОВАРЫ WHERE (ЦЕНА > 10000) ORDER BY ЦЕНА DESC)
	
SELECT Наименование_товара, Цена
FROM ТОВАРЫ
WHERE Цена = (SELECT MAX(Цена) FROM ТОВАРЫ);

SELECT Наименование_товара, Цена, 
       (SELECT MAX(Цена) FROM ТОВАРЫ) AS МаксЦена
FROM ТОВАРЫ;

SELECT Наименование_товара, Цена
FROM (SELECT Наименование_товара, Цена FROM ТОВАРЫ WHERE Цена > 5000) AS ВысокиеЦены;

SELECT Наименование_товара, Цена
FROM ТОВАРЫ A
WHERE Цена = (
    SELECT MAX(Цена)
    FROM ТОВАРЫ B
    WHERE A.Наименоввание_товара = B.Наименоввание_товара
);
